/******************************************************************************/
/*   check file access rights                                                 */
/******************************************************************************/
int checkFileRights( const char* fName, int mode )
{
  int rc ;
  int errRc = NO_ERROR ;

  rc = access( fName, mode ) ;

  if( rc != 0 ) errmsg( fName ) ;

_door:

  return errRc ;
}


